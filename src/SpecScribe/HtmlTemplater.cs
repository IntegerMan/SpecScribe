using System.Text;

namespace SpecScribe;

/// <summary>Wraps rendered markdown bodies in full HTML pages, and builds the generated site's index.</summary>
public static class HtmlTemplater
{

    public static string RenderPage(DocModel doc, SiteNav nav)
    {
        var prefix = PathUtil.RelativePrefix(doc.OutputRelativePath);
        var cssHref = prefix + ForgeOptions.StylesheetName;
        var scriptHref = prefix + ForgeOptions.ScriptName;

        var sb = new StringBuilder();
        sb.Append(PathUtil.RenderHeadOpen(
            $"{doc.Title} — {nav.SiteTitle}", cssHref, scriptHref,
            $"{doc.Title} — living documentation for {nav.SiteTitle}, generated by SpecScribe."));
        sb.Append(nav.RenderNavBar(doc.OutputRelativePath));
        sb.Append(SiteNav.RenderBreadcrumb(doc.OutputRelativePath, new (string, string?)[] { ("Home", "index.html"), (doc.Title, null) }));

        // Main content (header + article) is composed separately so it can be wrapped in the two-column page
        // shell alongside the TOC sidebar. Source render order equals DocModel.Headings order for a straight
        // full-page render, so those headings feed the shared TOC seam directly.
        var main = new StringBuilder();
        main.Append("<header class=\"doc-header\">\n");
        main.Append($"  <h1>{Html(doc.Title)}</h1>\n");
        if (doc.Frontmatter.Project is { Length: > 0 } project)
        {
            main.Append($"  <div class=\"doc-subtitle\">{Html(project)}</div>\n");
        }
        main.Append("  <div class=\"meta-pills\">\n");
        AppendPill(main, doc.Frontmatter.Author, "author");
        AppendPill(main, doc.Frontmatter.Date, "date");
        AppendPill(main, doc.Frontmatter.Version, v => $"v{v}");
        AppendStatusPill(main, doc.Frontmatter.Status);
        main.Append($"    <span class=\"pill\">{Html(PathUtil.NormalizeSlashes(doc.SourceRelativePath))}</span>\n");
        main.Append("  </div>\n</header>\n\n");

        // Spec-kernel bodies get the Capabilities list restyled into scannable cards; every other page's body
        // passes through untouched (the styler no-ops without the authored CAP pattern anyway). [Story 2.2 polish]
        var bodyHtml = IsSpecKernelPage(doc) ? CapabilityStyler.Style(doc.BodyHtml) : doc.BodyHtml;
        main.Append("<article class=\"doc-body\">\n");
        main.Append(bodyHtml);
        main.Append("\n</article>\n");

        var tocEntries = doc.Headings
            .Where(h => h.Level is 2 or 3)
            .Select(h => new Toc.Entry(h.Level, h.Text, h.Id))
            .ToList();
        sb.Append("<main id=\"main-content\">\n");
        // The companion-docs cross-link block (spec-kernel pages only) rides in the sidebar rail beneath the
        // TOC, not the content column, so it reads as a related-docs sidebar. [Story 2.2 polish]
        sb.Append(Toc.WrapWithSidebar(main.ToString(), tocEntries, BuildCompanionRail(doc)));
        sb.Append("</main>\n\n");

        sb.Append(PathUtil.RenderFooter($"on {DateTime.Now:yyyy-MM-dd HH:mm}"));
        if (doc.HasMermaid)
        {
            sb.Append(Mermaid.InitScript());
        }
        sb.Append("</body>\n</html>\n");
        return sb.ToString();
    }

    /// <summary>True for a page generated from a doc under the <c>specs/</c> kernel directory — the pages that
    /// get the capability-card and companion-rail treatment. Keyed by directory, disjoint from Story 2.1's
    /// <c>implementation-artifacts/spec-*.md</c>. [Story 2.2 polish]</summary>
    private static bool IsSpecKernelPage(DocModel doc) =>
        PathUtil.NormalizeSlashes(doc.SourceRelativePath).StartsWith("specs/", StringComparison.OrdinalIgnoreCase);

    /// <summary>Builds the "Companion documents" related-docs nav for the sidebar rail from a spec page's
    /// resolved companions, or null when there are none (so the rail shows only the TOC, or nothing). Each
    /// entry was resolved to a real generated page by the SiteGenerator — never a broken link. [Story 2.2 polish]</summary>
    private static string? BuildCompanionRail(DocModel doc)
    {
        if (doc.Companions.Count == 0) return null;

        var sb = new StringBuilder();
        sb.Append("<nav class=\"companion-docs\" aria-label=\"Companion documents\">\n");
        sb.Append("  <span class=\"companion-label\">Companion documents</span>\n");
        sb.Append("  <ul>\n");
        foreach (var (label, href) in doc.Companions)
        {
            sb.Append($"    <li><a href=\"{Html(href)}\">{Html(label)}</a></li>\n");
        }
        sb.Append("  </ul>\n</nav>\n");
        return sb.ToString();
    }

    public static string RenderIndex(IReadOnlyList<DocModel> docs, SiteNav nav, ProgressModel progress, EpicsModel? epicsModel, RequirementsModel? requirements, IReadOnlyList<AdrEntry> adrs, CommandCatalog commands, WorkInventory? work = null, SprintStatus? sprint = null, IReadOnlyList<RetroModel>? retros = null, ArtifactCoverage? coverage = null)
    {
        var inventory = work ?? WorkInventory.Empty;

        var groups = new (string Title, string Prefix)[]
        {
            ("Overview", ""),
            ("Planning Artifacts", "planning-artifacts"),
            // The spec kernel (SPEC.md + companions under specs/) is its own first-class band — the canonical
            // contract, so it sits between planning and implementation instead of falling into "Other". Keyed
            // on the specs/ directory prefix, disjoint from Story 2.1's implementation-artifacts/spec-*.md. The
            // empty-group guard below omits the section cleanly when no specs/ content exists. [Story 2.2 Task 2]
            ("Spec Kernel", "specs"),
            ("Implementation Artifacts", "implementation-artifacts"),
        };

        var sb = new StringBuilder();
        // Home page carries a descriptive title (sub-pages are already "Title — Site"); OG/description too. [Story 1.5 G2]
        sb.Append(PathUtil.RenderHeadOpen(
            $"{nav.SiteTitle} — Project Dashboard", ForgeOptions.StylesheetName, ForgeOptions.ScriptName,
            $"Project dashboard and living documentation for {nav.SiteTitle}, generated by SpecScribe."));
        sb.Append(nav.RenderNavBar(SiteNav.HomeOutputPath));

        // Single <main id="main-content"> landmark wrapping the dashboard + index sections — the skip
        // link's target and the one main region on the page. [Story 1.4 AC #1, UX-DR16]
        sb.Append("<main id=\"main-content\">\n");
        sb.Append("<header class=\"doc-header\">\n");
        sb.Append($"  <h1>{Html(nav.SiteTitle)}</h1>\n");
        sb.Append("</header>\n\n");

        AppendDashboard(sb, progress, epicsModel, requirements, nav, commands, inventory, sprint, coverage);

        // Quick-dev + deferred work is surfaced in its own first-class section (below); promote those docs
        // out of the generic "Implementation Artifacts" grid so they aren't double-listed. Their standalone
        // pages still exist and stay navigable — this only affects the index grouping (mirrors how README is
        // kept out of the grid). [Story 2.1 Task 2]
        AppendWorkTypesSection(sb, inventory, sprint);

        var promotedOutputs = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        foreach (var q in inventory.QuickDev) promotedOutputs.Add(q.OutputPath);
        if (inventory.Deferred is { } def) promotedOutputs.Add(def.OutputPath);

        var used = new HashSet<DocModel>(
            docs.Where(d => promotedOutputs.Contains(PathUtil.NormalizeSlashes(d.OutputRelativePath))));

        foreach (var (groupTitle, groupPrefix) in groups)
        {
            var inGroup = docs
                .Where(d => !used.Contains(d))
                .Where(d => groupPrefix.Length == 0
                    ? !PathUtil.NormalizeSlashes(d.SourceRelativePath).Contains('/')
                    : PathUtil.NormalizeSlashes(d.SourceRelativePath).StartsWith(groupPrefix + "/", StringComparison.OrdinalIgnoreCase))
                .OrderBy(d => d.Title, StringComparer.OrdinalIgnoreCase)
                .ToList();

            if (inGroup.Count == 0) continue;
            foreach (var d in inGroup) used.Add(d);

            // The Planning Artifacts band is regrouped by well-known kind (PRD prominent primary, UX Design +
            // Experience paired, Brief distinct) with the PRD's quality-review rubric folded under the PRD,
            // rather than dumped alphabetically. Every other band keeps the flat title + grid. [Story 2.4 Task 3/4]
            if (groupPrefix == "planning-artifacts")
            {
                AppendPlanningSection(sb, inGroup);
                continue;
            }

            sb.Append($"<div class=\"index-section-title\">{Icons.ForConcept(groupTitle)}{Html(groupTitle)}</div>\n");
            sb.Append("<div class=\"index-grid\">\n");
            foreach (var d in inGroup)
            {
                AppendIndexCard(sb, d);
            }
            sb.Append("</div>\n\n");
        }

        var remaining = docs.Where(d => !used.Contains(d)).OrderBy(d => d.Title, StringComparer.OrdinalIgnoreCase).ToList();
        if (remaining.Count > 0)
        {
            sb.Append("<div class=\"index-section-title\">Other</div>\n<div class=\"index-grid\">\n");
            foreach (var d in remaining)
            {
                AppendIndexCard(sb, d);
            }
            sb.Append("</div>\n\n");
        }

        AppendAdrSection(sb, adrs);
        AppendRetrosSection(sb, retros);

        sb.Append("</main>\n\n");
        sb.Append(PathUtil.RenderFooter($"on {DateTime.Now:yyyy-MM-dd HH:mm}"));
        sb.Append("</body>\n</html>\n");
        return sb.ToString();
    }

    /// <summary>The home "Retrospectives" band: one card per retrospective note (its dedicated page), so the
    /// retros stay visible on home now that they're consumed out of the generic artifact grid. Omitted when
    /// there are none. [Story 2.3 retro pages]</summary>
    private static void AppendRetrosSection(StringBuilder sb, IReadOnlyList<RetroModel>? retros)
    {
        if (retros is null || retros.Count == 0) return;

        sb.Append("<div class=\"index-section-title\">Retrospectives</div>\n");
        sb.Append("<div class=\"index-grid\">\n");
        foreach (var r in retros)
        {
            sb.Append($"  <a class=\"index-card\" href=\"{Html(PathUtil.NormalizeSlashes(r.OutputRelativePath))}\">\n");
            sb.Append($"    <h2>{Html(r.Title)}</h2>\n");
            if (r.DateText is { Length: > 0 } d)
            {
                sb.Append($"    <p>{Html(d)}</p>\n");
            }
            sb.Append($"    <span class=\"index-card-path\">{Html(PathUtil.NormalizeSlashes(r.SourceRelativePath))}</span>\n");
            sb.Append("  </a>\n");
        }
        sb.Append("</div>\n\n");
    }

    private static void AppendDashboard(StringBuilder sb, ProgressModel p, EpicsModel? epicsModel, RequirementsModel? requirements, SiteNav nav, CommandCatalog commands, WorkInventory work, SprintStatus? sprint = null, ArtifactCoverage? coverage = null)
    {
        sb.Append("<section class=\"dashboard\">\n");

        // Stat headline row. Labels pluralize (A2), the task stat is scoped so it can't read as "62% of the
        // project is done" (A5), the commit stat carries a recency signal (F3), and each card defines what it
        // actually counts via an on-brand tooltip (C2).
        sb.Append("<div class=\"stat-grid\">\n");
        sb.Append(Charts.StatCard($"{p.EpicsDrafted}/{p.EpicsTotal}", "Epics drafted", tooltip: "Epics with at least one story drafted, out of all epics."));
        sb.Append(Charts.StatCard(p.StoriesTotal.ToString(), "Stories defined", $"{p.StoriesWithArtifact} with a task plan",
            "Stories listed across every epic; the sub-line counts those with a BMad task checklist."));
        sb.Append(p.TasksTotal > 0
            ? Charts.StatCard($"{p.TasksDone}/{p.TasksTotal}", "Planned tasks done", $"{p.StoriesWithArtifact}/{p.StoriesTotal} stories planned",
                $"Checklist tasks done across the {p.StoriesWithArtifact} stories that have a task plan — not the whole project.")
            : Charts.StatCard("—", "Planned tasks done", "none tracked yet"));
        sb.Append(p.Git is { } git
            ? Charts.StatCard(git.TotalCommits.ToString(), Charts.Plural(git.TotalCommits, "Commit", "Commits"), CommitStatSub(git),
                "Total commits in the repository; the sub-line shows active days and how recently work landed.")
            : Charts.StatCard("—", "Commits", "no git history"));
        // Quick-dev / deferred work as a SEPARATE whole-project signal so the picture is complete — never
        // folded into the epic/story/task tallies above (that would misrepresent completion; AC #1). The card
        // is conditional, so a project with no such work keeps the existing four-card row byte-for-byte. [Story 2.1 Task 3]
        if (!work.IsEmpty)
        {
            var deferredCount = work.Deferred?.OpenItemCount ?? 0;
            var sub = work.Deferred is not null
                ? $"{deferredCount} deferred {Charts.Plural(deferredCount, "item", "items")}"
                : "outside the epic plan";
            sb.Append(Charts.StatCard(work.QuickDev.Count.ToString(), "Direct changes", sub,
                "Quick-dev / one-shot changes and deferred-work notes — tracked separately from the epic/story plan, never counted as epic or story completion."));
        }
        sb.Append("</div>\n\n");

        // Now & Next: when a sprint is tracked this panel BECOMES the sprint board (the authoritative tracked
        // view, capped at 3/column with a CTA to the full page); with no sprint it falls back to the derived
        // in-dev/review/up-next view from each story artifact's own status. [Story 2.3]
        if (epicsModel is not null)
        {
            AppendNowAndNext(sb, epicsModel, sprint);
        }

        // The sunburst (whole-project map) is the dashboard's headline panel.
        if (epicsModel is not null)
        {
            sb.Append("<div class=\"chart-panel sunburst-panel\">\n");
            sb.Append("<div class=\"chart-panel-header-row\"><h3>Project at a Glance</h3>");
            sb.Append("<a class=\"view-epic-link\" href=\"epics.html\">View Epics &amp; Stories &rarr;</a></div>\n");
            sb.Append(Charts.Sunburst(epicsModel, commands: commands));
            sb.Append("</div>\n\n");
        }

        // Two compact panels share a row — the epic-status donut and the overall-progress bars — freeing full
        // width below for the consolidated Git Pulse panel. [Story 3.1 consolidation]
        sb.Append("<div class=\"chart-row\">\n");
        AppendEpicStatusPanel(sb, p, epicsModel);

        sb.Append("<div class=\"chart-panel\">\n<h3>Overall Progress</h3>\n");
        sb.Append(Charts.ProgressBar("Planning", p.EpicsDrafted, p.EpicsTotal, $"{p.EpicsDrafted} / {p.EpicsTotal} epics"));
        sb.Append(p.TasksTotal > 0
            ? Charts.ProgressBar("Implementation", p.TasksDone, p.TasksTotal, $"{p.TasksDone} / {p.TasksTotal} tasks ({p.StoriesWithArtifact} of {p.StoriesTotal} stories planned)")
            : Charts.ProgressBar("Implementation", 0, 0, "not started"));
        sb.Append("</div>\n");

        sb.Append("</div>\n\n");

        // Refinement funnel — planning maturity at a glance (epics → drafted stories → task plans → tasks),
        // a peer of the progress panels above the per-repo activity detail. Rendered unconditionally: the
        // builder owns the empty-state (ProgressModel.Empty → the shared chart-empty placeholder), so the
        // templater never grows a second, divergent fallback. [Story 3.6]
        sb.Append("<div class=\"chart-panel funnel-panel\">\n<h3>Refinement Funnel</h3>\n");
        sb.Append(Charts.RefinementFunnel(p));
        sb.Append("</div>\n\n");

        // One consolidated Git Pulse panel — headline signal strip + activity heatmap + top-changed-files bars —
        // replacing the previously separate "Commit Activity" and "Git Pulse" panels (owner review: combine and
        // make it graphical, not two plain boxes). The "Commits" stat card at the top of the dashboard still
        // shows the whole-repo total; this panel is the richer at-a-glance activity surface. Single non-fatal
        // fallback: the UX spec's "—" + tooltip. [Story 3.1 AC #3; EXPERIENCE.md:169]
        sb.Append("<div class=\"chart-panel git-pulse-panel\">\n");
        // When deep analytics were generated (--deep-git), the Git Pulse header gains its upper-right entry
        // links: "View all git insights →" to the aggregate hub (Story 3.8 — the primary route in; gated on
        // DeepGit.Insights, which SiteGenerator clears if the hub page wasn't actually written, so the link
        // can never dangle) and "View Deep Analytics →" to the coupling/hotspots page (Story 3.2). Both are
        // absent (plain heading) when the flag is off, so the default dashboard is unchanged.
        if (p.DeepGit is not null)
        {
            sb.Append("<div class=\"chart-panel-header-row\"><h3>Git Pulse</h3><span class=\"git-pulse-header-links\">");
            if (p.DeepGit.Insights is not null)
            {
                sb.Append($"<a class=\"view-epic-link\" href=\"{SiteNav.GitInsightsOutputPath}\">View all git insights &rarr;</a>");
            }
            sb.Append($"<a class=\"view-epic-link\" href=\"{SiteNav.DeepAnalyticsOutputPath}\">View Deep Analytics &rarr;</a></span></div>\n");
        }
        else
        {
            sb.Append("<h3>Git Pulse</h3>\n");
        }
        sb.Append(p.Git is { } pulse
            ? Charts.GitPulsePanel(pulse)
            : "<div class=\"chart-empty git-pulse-empty\" data-tooltip=\"Run in a git repository to enable commit stats\" tabindex=\"0\">—</div>\n");
        sb.Append("</div>\n\n");

        // Planning Coverage — the artifact-family-coverage half of CAP-4, a peer of the Git Pulse panel above.
        // Rendered ONLY when at least one canonical family was recognized; a repo with no recognized families
        // keeps today's dashboard byte-for-byte unchanged (graceful omission, Story 1.1). [Story 3.3 AC #1]
        if (coverage is { IsEmpty: false })
        {
            var coverageToday = DateOnly.FromDateTime(DateTime.Now);
            sb.Append("<div class=\"chart-panel coverage-panel\">\n");
            // Heading left, the compact coverage meter pinned top-right (mirrors the sunburst/requirements
            // header-row pattern) so the bar is a small at-a-glance summary, not a dominant band.
            sb.Append("<div class=\"chart-panel-header-row\"><h3>Planning Artifacts</h3>");
            sb.Append(Charts.CoverageMeter(coverage, coverageToday));
            sb.Append("</div>\n");
            sb.Append(Charts.ArtifactCoveragePanel(coverage, coverageToday));
            sb.Append("</div>\n\n");
        }

        // Deep analytics (hotspots + change-coupling graph) live on their own dedicated page now, not inline on
        // the dashboard — the Git Pulse header above links out to it when --deep-git produced data. This keeps
        // the dashboard focused and gives the graph room to breathe. [Story 3.2]

        AppendRequirementsPanel(sb, requirements);

        if (p.PerEpic.Count > 0)
        {
            sb.Append("<div class=\"chart-panel\">\n<h3>Progress by Epic</h3>\n");
            sb.Append(Charts.EpicMosaic(p.PerEpic, e => $"epics/epic-{e.Number}.html"));
            sb.Append("</div>\n\n");
        }

        // "Explore Key Views" duplicates the top nav and the bottom index sections, so it's slimmed to a
        // compact pill row and moved down here — below the substance rather than above it (F1).
        AppendDashboardQuickLinks(sb, nav);

        sb.Append("</section>\n\n");
    }

    /// <summary>The commit stat's sub-line: active days plus a recency signal so "how recently did work land"
    /// is answered above the fold. [Story 1.5 F3]</summary>
    private static string CommitStatSub(GitPulse git)
    {
        var daysAgo = DateOnly.FromDateTime(DateTime.Now).DayNumber - git.LastCommitDate.DayNumber;
        var recency = daysAgo <= 0 ? "last commit today"
            : daysAgo == 1 ? "last commit yesterday"
            : $"last commit {daysAgo}d ago";
        return $"{git.ActiveDays} active {Charts.Plural(git.ActiveDays, "day", "days")} · {recency}";
    }

    /// <summary>The Epic Status donut. When the epics model is available its segments come from the same
    /// <see cref="StatusStyles.ForEpic"/> story roll-up the sunburst uses (done/active/drafted/pending) so the
    /// donut stops contradicting the sunburst; the center reads as a done/total fraction and zero-count legend
    /// rows are suppressed. Falls back to the binary drafted/pending split when no epics model exists. [Story 1.5 A3/B4/E3]</summary>
    private static void AppendEpicStatusPanel(StringBuilder sb, ProgressModel p, EpicsModel? epicsModel)
    {
        sb.Append("<div class=\"chart-panel\">\n<h3>Epic Status</h3>\n<div class=\"donut-and-legend\">\n");

        List<(string Label, int Value, string CssClass)> segments;
        string? centerText;
        if (epicsModel is not null && epicsModel.Epics.Count > 0)
        {
            var classes = epicsModel.Epics.Select(StatusStyles.ForEpic).ToList();
            int Count(string c) => classes.Count(x => x == c);
            // Bucket every class ForEpic can return, driven by the single StatusStyles.EpicStages list, so a
            // future epic tier can never silently drop out of the donut (and disagree with the center fraction).
            segments = StatusStyles.EpicStages
                .Select(stage => (StatusStyles.EpicLabel(stage), Count(stage), stage))
                .ToList();
            centerText = $"{Count("done")}/{classes.Count}";
        }
        else
        {
            segments = new List<(string, int, string)>
            {
                ("Stories drafted", p.EpicsDrafted, "drafted"),
                ("Pending", p.EpicsPending, "pending"),
            };
            // Without a parsed epics model the per-epic done state is unknown, so show the epic count rather
            // than fabricating a "0/N" fraction that would assert zero progress even for completed epics. [review]
            centerText = null;
        }

        var nonZero = segments.Where(s => s.Value > 0).ToList();
        var ariaParts = nonZero.Count > 0
            ? string.Join(", ", nonZero.Select(s => $"{s.Value} {s.Label.ToLowerInvariant()}"))
            : "no epics yet";

        sb.Append(Charts.Donut(segments, ariaLabel: $"Epic status: {ariaParts}", centerText: centerText));
        // Only non-zero rows are rendered (B4) — no more "Done (0), Ready (0)" noise.
        sb.Append(Charts.DonutLegend(nonZero));
        sb.Append("</div>\n</div>\n\n");
    }

    /// <summary>Surfaces what's in motion right now. When an active sprint is tracked (<paramref name="sprint"/>
    /// present and non-empty), this panel BECOMES the sprint board — the authoritative tracked view — capped at
    /// 3 cards per column with a "+N more" link to the full sprint page, plus a progress wheel and a "View sprint
    /// board" CTA in the header. With no sprint it falls back to the derived in-dev/review/up-next/next-to-draft
    /// view from each story artifact's own status. The two status sources stay labeled and distinct (Two Status
    /// Sources). [Story 2.3]</summary>
    private static void AppendNowAndNext(StringBuilder sb, EpicsModel epicsModel, SprintStatus? sprint)
    {
        if (sprint is { IsEmpty: false })
        {
            sb.Append("<div class=\"chart-panel sprint-board-panel\">\n");
            // One header row: the title + its source label (inline, so it doesn't cost its own line), a status
            // progress wheel, and the CTA. [Story 2.3 redesign]
            sb.Append("<div class=\"chart-panel-header-row sprint-board-header\">\n");
            sb.Append("  <h3>Now &amp; Next <span class=\"panel-source-inline\">from sprint-status.yaml</span></h3>\n");
            sb.Append("  <div class=\"sprint-board-header-aside\">");
            sb.Append(SprintTemplater.RenderProgressWheel(sprint));
            sb.Append($"<a class=\"view-epic-link\" href=\"{SiteNav.SprintOutputPath}\">View sprint board &rarr;</a>");
            sb.Append("</div>\n</div>\n");
            sb.Append(SprintTemplater.RenderBoard(sprint, epicsModel, capPerColumn: 3, moreHref: SiteNav.SprintOutputPath));
            sb.Append("</div>\n\n");
            return;
        }

        var allStories = epicsModel.Epics.SelectMany(e => e.Stories.Select(s => (Epic: e, Story: s))).ToList();
        var inDev = allStories.Where(x => StatusStyles.ForStory(x.Story) == "active").ToList();
        var inReview = allStories.Where(x => StatusStyles.ForStory(x.Story) == "review").ToList();
        var upNext = allStories.Where(x => StatusStyles.ForStory(x.Story) == "ready").ToList();

        // The lowest-numbered story that's been listed in a drafted epic but has no artifact yet — the
        // next stub to flesh out. Pending epics are covered by nextEpicToDraft, so exclude them here.
        var nextStoryToDraft = allStories
            .Where(x => x.Epic.Status == EpicStatus.Drafted && StatusStyles.ForStory(x.Story) == "drafted")
            .OrderBy(x => x.Epic.Number)
            .ThenBy(x => StoryMinor(x.Story.Id))
            .Select(x => (x.Epic, x.Story))
            .FirstOrDefault();

        var nextEpicToDraft = epicsModel.Epics.OrderBy(e => e.Number).FirstOrDefault(e => e.Status == EpicStatus.Pending);

        if (inDev.Count == 0 && inReview.Count == 0 && upNext.Count == 0
            && nextStoryToDraft.Story is null && nextEpicToDraft is null) return;

        sb.Append("<div class=\"chart-panel\">\n<h3>Now &amp; Next</h3>\n<div class=\"now-next\">\n");

        foreach (var (epic, story) in inDev)
        {
            AppendNowNextCard(sb, "active", "In development",
                $"Story {story.Id} · {PathUtil.StripHtmlTags(story.Title)}",
                story.ArtifactOutputPath ?? $"epics/epic-{epic.Number}.html");
        }

        foreach (var (epic, story) in inReview)
        {
            AppendNowNextCard(sb, "review", "In review",
                $"Story {story.Id} · {PathUtil.StripHtmlTags(story.Title)}",
                story.ArtifactOutputPath ?? $"epics/epic-{epic.Number}.html");
        }

        foreach (var (epic, story) in upNext)
        {
            AppendNowNextCard(sb, "ready", "Up next",
                $"Story {story.Id} · {PathUtil.StripHtmlTags(story.Title)}",
                story.ArtifactOutputPath ?? $"epics/epic-{epic.Number}.html");
        }

        if (nextStoryToDraft.Story is not null)
        {
            var (epic, story) = nextStoryToDraft;
            AppendNowNextCard(sb, "drafted", "Next story to draft",
                $"Story {story.Id} · {PathUtil.StripHtmlTags(story.Title)}",
                story.ArtifactOutputPath ?? $"epics/epic-{epic.Number}.html");
        }

        if (nextEpicToDraft is not null)
        {
            AppendNowNextCard(sb, "pending", "Next epic to draft",
                $"Epic {nextEpicToDraft.Number} · {PathUtil.StripHtmlTags(nextEpicToDraft.Title)}",
